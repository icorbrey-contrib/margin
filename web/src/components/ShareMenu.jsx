export default function ShareMenu({ uri, text, customUrl }) {
  const [isOpen, setIsOpen] = useState(false);
  const [copied, setCopied] = useState(false);
  const menuRef = useRef(null);

  const getShareUrl = () => {
    if (customUrl) return customUrl;
    if (!uri) return "";
    const uriParts = uri.split("/");
    const did = uriParts[2];
    const rkey = uriParts[uriParts.length - 1];
    return `${window.location.origin}/at/${did}/${rkey}`;
  };

  const shareUrl = getShareUrl();

  useEffect(() => {
    const handleClickOutside = (e) => {
      if (menuRef.current && !menuRef.current.contains(e.target)) {
        setIsOpen(false);
      }
    };
    if (isOpen) {
      document.addEventListener("mousedown", handleClickOutside);
    }
    return () => document.removeEventListener("mousedown", handleClickOutside);
  }, [isOpen]);

  const handleShareToFork = (domain) => {
    const composeText = text
      ? `${text.substring(0, 200)}...\n\n${shareUrl}`
      : shareUrl;
    const composeUrl = `https://${domain}/intent/compose?text=${encodeURIComponent(composeText)}`;
    window.open(composeUrl, "_blank");
    setIsOpen(false);
  };

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(shareUrl);
      setCopied(true);
      setTimeout(() => {
        setCopied(false);
        setIsOpen(false);
      }, 1500);
    } catch {
      prompt("Copy this link:", shareUrl);
    }
  };

  const handleSystemShare = async () => {
    if (navigator.share) {
      try {
        await navigator.share({
          title: "Margin Annotation",
          text: text?.substring(0, 100),
          url: shareUrl,
        });
      } catch {}
    }
    setIsOpen(false);
  };

  return (
    <div className="share-menu-container" ref={menuRef}>
      <button
        className="annotation-action"
        onClick={() => setIsOpen(!isOpen)}
        title="Share"
      >
        <svg
          width="18"
          height="18"
          viewBox="0 0 24 24"
          fill="none"
          stroke="currentColor"
          strokeWidth="2"
          strokeLinecap="round"
          strokeLinejoin="round"
        >
          <circle cx="18" cy="5" r="3" />
          <circle cx="6" cy="12" r="3" />
          <circle cx="18" cy="19" r="3" />
          <line x1="8.59" y1="13.51" x2="15.42" y2="17.49" />
          <line x1="15.41" y1="6.51" x2="8.59" y2="10.49" />
        </svg>
      </button>

      {isOpen && (
        <div className="share-menu">
          <div className="share-menu-section">
            <div className="share-menu-label">Share to</div>
            {BLUESKY_FORKS.map((fork) => (
              <button
                key={fork.domain}
                className="share-menu-item"
                onClick={() => handleShareToFork(fork.domain)}
              >
                <span className="share-menu-icon">
                  <fork.Icon />
                </span>
                <span>{fork.name}</span>
              </button>
            ))}
          </div>
          <div className="share-menu-divider" />
          <button className="share-menu-item" onClick={handleCopy}>
            {copied ? <Check size={16} /> : <Copy size={16} />}
            <span>{copied ? "Copied!" : "Copy Link"}</span>
          </button>
          {navigator.share && (
            <button className="share-menu-item" onClick={handleSystemShare}>
              <ExternalLink size={16} />
              <span>More...</span>
            </button>
          )}
        </div>
      )}
    </div>
  );
}
